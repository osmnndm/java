package day02wrapperclasses;

public class Wrapper01 {

	public static void main(String[] args) {
		
		// int data tipinin max ve min değerlerini bulup ekrana yazdırınız.
		// int in wrapper class'ı Integer
		
		System.out.println("DATA TYPLERİNİN MAX VE MİN DEĞERLERİNİ YAZDIRMA");
		System.out.println();
		System.out.println("INTEGER");
		System.out.println(Integer.MAX_VALUE);
		System.out.println(Integer.MIN_VALUE);
		System.out.println();
		
		// bir Class'ın içindeki metodlara ulaşmak için 
		// class isminden sonra . koyuyoruz, 
		// çıkan listeden seçim yapıyoruz.
		
		
		// long data tipinin max ve min değerlerini bulup ekrana yazdırınız
		
		System.out.println("LONG");
		System.out.println(Long.MAX_VALUE);
		System.out.println(Long.MIN_VALUE);
		System.out.println();

		// ODEV: TÜM PRİMİTİVLERİN MAX VE MİN DEĞERLERİNİN WRAPPER CLASSLARINI KULLANARAK EKRANA YAZDIRINIZ
		
		System.out.println("BYTE");
		System.out.println(Byte.MAX_VALUE );
		System.out.println(Byte.MIN_VALUE );
		System.out.println();
		
		
		System.out.println("SHORT");
		System.out.println(Short.MAX_VALUE);
		System.out.println(Short.MIN_VALUE );
		System.out.println();
		
		
		System.out.println("FLOAT");
		System.out.println(Float.MAX_VALUE);
		System.out.println(Float.MIN_VALUE);
		System.out.println();
		
		
		System.out.println("DOUBLE");
		System.out.println(Double.MAX_VALUE);
		System.out.println(Double.MAX_VALUE);
		System.out.println();
		
		
		System.out.println("CHAR");
		System.out.println(Character.MAX_VALUE);
		System.out.println(Character.MIN_VALUE );
		
		
		
		
		
		
		
	}

}
