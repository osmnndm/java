
public class Increment04 {

	public static void main(String[] args) {
		
		
		// int data type inde bir tane variable oluşturun
		// bu variable ı her seferinde bir artırmak için üç yöntemide kullanın
		
		int num1=12;
		// uzun yol
		num1 = num1+1;
		System.out.println(num1); // 13
		
		//kısa yol
		num1+=1;
		System.out.println(num1); // 14
		
		// en kısa yol
		num1++;
		System.out.println(num1); // 15
		
		
		
		
		
		
		
		
		
		
		

	}

}
