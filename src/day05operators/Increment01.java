package day05operators;

public class Increment01 {

	public static void main(String[] args) {
		// int data type'inde iki variable oluşturun
		// birincisini 3 ikincisini 7 artırın
		
		int sayi1=12;
		sayi1= sayi1+3;  // bu tanımlamaya increment deniyor.
		System.out.println(sayi1);
		
		int sayi2=18;
		sayi2=sayi2+7;
		System.out.println(sayi2);
		
		

	}

}
