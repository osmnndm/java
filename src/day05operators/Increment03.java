package day05operators;

public class Increment03 {

	public static void main(String[] args) {
	
		// int data typ inde bir tane variable oluşturun.
		// bu variable ları EN KISA yolu kullanarak 1 artırın
		// en kısa yol sadece 1 artırmak için kullanılır.
		
		int sayi1=12;
		sayi1++;    // variable isminden sonra iki kere artı sembolu yazarsanız
		           // bu variablenin değerini bir artırmış olursunuz.
		
		System.out.println(sayi1);
		
		
		
		int sayi2=10;
		sayi2++;
		System.out.println(sayi2);
		
		
		

	}

}
