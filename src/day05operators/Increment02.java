package day05operators;

public class Increment02 {

	public static void main(String[] args) {
		
		// iki tane int data type nda variable oluşturun
		// birinciyi 3, ikinciyi 7 artırın
		// kısa yol kullan
		
		int sayi1=12;
		sayi1+=3;  // + ile = arasına boşluk bırakmayın.
		System.out.println(sayi1);
		
		int sayi2=18;
		sayi2+=7;
		System.out.println(sayi2);
		
		
		

		

	}

}
