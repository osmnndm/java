
public class IncrementDecrement {

	public static void main(String[] args) {
		
		// int data type inde bir variable oluşturun.
		// bu variable in değerini çarpma ve bölme kullanarak artırın ve azaltın
		
		
		int num1=12;
		// uzun yol kullanarak çarpma yaparak increment
		
		num1=num1*3;  // 36
		System.out.println(num1);
		
		// kısa yol ile increment
		num1*=3;  // 108
		System.out.println(num1);
		
		
		// uzun yol ile bölme yaparak dicrement
		num1=num1/3;
		System.out.println(num1);  // 36
		
		// kısa yol ile bölme yaparak decrement
		num1/=3; // 12
		System.out.println(num1);
		
		

	}

}
