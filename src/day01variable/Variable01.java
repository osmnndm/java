package day01variable;

public class Variable01 {
	
public static void main(String[] args) {
	int maas=3000;
	
	char harf ='S';   //charlara değer ataması yaparken mutlaka tek tırnak kullan!
	
	System.out.println(maas);
	System.out.println(harf);
	
	int sayi =123;
	
	System.out.println(sayi);
	
	boolean isOld= true; // boolean'lar sadece true veya false değerlerini kullanabilirsiniz.
	
	System.out.println(isOld); // true
	
	boolean isNew =  false;
	
	System.out.println(isNew); // false
	
	

	short nazilliNufus=28000;
	
	System.out.println(nazilliNufus);
	
	long hucreSayisi=353445435;
	
	System.out.println(hucreSayisi);
	
	double pi = 3.14252352352;
	System.out.println(pi); // java bütün ondalık kesirleri otomatik olarak double kabul eder.
	
	
	float para = 5.25f;
	
	System.out.println(para);
	
	String ogrenciIsmi = "Ali Can";
	
	System.out.println(ogrenciIsmi);
	
	
	
	
	
}
	
	
	
	
	

}
